    private void setDateTime(){
        datePicker1.addDateChangeListener(new DateChangeListener() {
            @Override
            public void dateChanged(DateChangeEvent dce) {
                date = PickerUtilities.localDateToString(dce.getNewDate(), null);
                System.out.println(date);
            }
        });
        
        timePicker1.addTimeChangeListener(new TimeChangeListener() {
            @Override
            public void timeChanged(TimeChangeEvent tce) {
                System.out.println(tce.getNewTime());
                time = PickerUtilities.localTimeToString(tce.getNewTime(), null);
                System.out.println(time);
            }
        });
    }

    
