    private void loadPhotographer(){
        try {
            String query = "SELECT name FROM employee WHERE employee_type = 'PHOTOGRAPER'";
 
            ResultSet data = MyConnection.getData(query);
            
            Vector v1= new Vector();
            while (data.next()) {                
                v1.add(data.getString("name"));
            }
            jComboBox1.setModel(new DefaultComboBoxModel(v1));
            
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
