      public ArrayList<addprojectbean>retrieveData(){
        
        ArrayList<addprojectbean> al=new ArrayList<>();
        
        try{
            String query = "SELECT * FROM event e, employee e1,client c where e.date = '"+Calender.passDate+"' and e.employee_idemployee=e1.idemployee and e.client_idclient=c.idclient group by e.id";
            ResultSet rs= MyConnection.getData(query);
            addprojectbean task;
        
            while(rs.next()){
            task=new addprojectbean(rs.getInt("id"),rs.getString("event_name"),rs.getString("venue"),rs.getString("date"),rs.getString("time"),rs.getString("price"),rs.getString("client_name"),rs.getString("name"));
             al.add(task);
            } 
        }
        
        
        catch(Exception e){
            System.out.println(e);
            
        }
        
        return al;
        
    }
      
      
    
    public void fillTable(){
        
        ArrayList<addprojectbean> al=retrieveData();
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        
        Object[]row=new Object[8];
                
                for(int i=0; i<al.size(); i++){
                    
                    row[0]=al.get(i).getid();
                    row[1]=al.get(i).getevent_name();
                    row[2]=al.get(i).getvenue();
                    row[3]=al.get(i).getdate();
                    row[4]=al.get(i).gettime();
                    row[5]=al.get(i).getprice();
                    row[6]=al.get(i).getclient_idclient();
                    row[7]=al.get(i).getemployee_idemployee();
                    model.addRow(row);
                }
        
        
    }
    
    
    
     
